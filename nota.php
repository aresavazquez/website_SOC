			<p>Banca Afirme decidió agilizar el trámite y los requisitos para que las pequeñas y medianas empresas (Pymes) adquieran un crédito de liquidez empresarial con sólo presentar los estados de cuenta para comprobar ingresos facilitando su obtención de la mano de un Asesor SOC quien, a través de su área como especialista, se encargará de acompañar al empresario durante todo el proceso.</p>
			<p>Dicho esquema aplica para los dueños de Pymes ubicadas en las ciudades donde el banco tiene presencia (Centro, Occidente y Norte de la República Mexicana), por lo que ya no deberán presentar ni declaraciones de impuestos ni estados financieros para obtener los recursos.</p>
			<p>De acuerdo con Jacobo Gracida Blancas, Director Nacional Pyme de SOC Asesores Hipotecarios, el préstamo para capital de trabajo podrá ser de dos a cuatro meses del saldo promedio mensual que muestre la empresa.Dicho préstamo también se puede generar bajo el análisis de la facturación en la Terminal Punto de Venta (TPV) del negocio, en donde el banco puede otorgar un crédito simple o revolvente de entre dos y cuatro meses del promedio de facturación.</p>
			<p>Dicho préstamo también se puede generar bajo el análisis de la facturación en la Terminal Punto de Venta (TPV) del negocio, en donde el banco puede otorgar un crédito simple o revolvente de entre dos y cuatro meses del promedio de facturación. “Al eliminar requisitos como los estados financieros, con este producto estamos viendo una forma de ayudar a las empresas a tener una mayor capacidad de respuesta ante distintas necesidades o imprevistos y para invertir con facilidad en capital de trabajo” dijo Gracida Blancas. “El crédito para una empresa es de suma importancia porque le ofrece liquidez para poder comprar a buen precio materiales, hacer pagos a tiempo o imprevistos; incluso aprovechar oportunidades de negocio, etc. Un crédito como éste puede hacer la diferencia entre desarrollar o no un nuevo proyecto”, agregó. De acuerdo con el especialista, en seis meses Banca Afirme puede re-evaluar el desempeño financiero de la empresa e incrementar hasta el doble el préstamo previamente adquirido.
			<br><br>
			Más fácil con tu Asesor SOC
			<br><br>
			Debido a que los empresarios carecen de tiempo y en muchas ocasiones de conocimiento de la oferta de créditos, hoy en día pueden apoyarse en un Asesor SOC de la División de Crédito Empresarial, para solicitar este financiamiento y que éste también se encargue de todos los trámites evitando distraerse de su negocio. “Estamos conscientes de que los empresarios no pueden dedicarse a actividades fuera del negocio; por eso en SOC Asesores Hipotecarios tenemos especialistas que pueden visitarlos en sus oficinas, integrar su expediente y tramitar su crédito totalmente gratis”, agregó Gracida Blancas.</p>
